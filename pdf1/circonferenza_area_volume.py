# Esercizio 8. Usando la costate π approssimata pi = 3.1415926536 e dato r = 2.5, calcolare:
# 1. La circonferenza di raggio r: circ = 2πr
# 2. L'area di un cerchio di raggio r: area = πr2?
# 3. Il volume di una sfera di raggio r: vol = 4/3*πr3.

pi = 3.1415926536
r = 2.5

print("circonferenza è:", 2 * pi * r)
print("area è: ", pi * r**2)
print("volume è", 4 / 3 * pi * r**3)
